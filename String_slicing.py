# ---------------string_slicing_&_other_function---------------------
mystr=   "my name is akhil"

print(mystr[:7]) #taking lenght of the string
print(mystr[::2]) #skiping two numbers
print(mystr[::-1]) #reversing a function

#------------------functions---------------------------------------

print(len(mystr)) #lenght of the function
print(mystr.isalnum()) #checking string is alpha numeric or not
print(mystr.isalpha()) #checking string is alpha
print(mystr.endswith("akhil")) #checking end of the string
print(mystr.count("a")) #counting string value
print(mystr.capitalize()) #capitalized first word
print(mystr.find("is"))# finding the position of "is"
print(mystr.upper()) #converting string to upper case
print(mystr.lower()) #converting string to lower case
print(mystr.replace("is","are")) #replacing is with are


